def main():
    # 读取原始数据
    with open(BASE / 'questions.json', 'r', encoding='utf-8') as f:
        questions = json.load(f)

    print(f"读取 {len(questions)} 道题")

    # 统计
    from collections import Counter
    kp_counter = Counter()
    unmatched = []

    # 为每道题标注知识点
    for q in questions:
        key, name = match_question(q)
        if key is None or key == 'general':
            unmatched.append(q['id'])
        q['knowledge_point'] = name if name else '综合/未分类'
        q['knowledge_point_key'] = key if key else 'general'
        kp_counter[name] += 1

    # 输出统计
    print("\n=== Knowledge Point Distribution ===")
    for name, count in kp_counter.most_common():
        pct = count / len(questions) * 100
        bar = '#' * int(pct / 2)
        print(f"  {name:20s}  {count:4d}  ({pct:5.1f}%)  {bar}")

    if len(unmatched) > 20:
        print(f"\nUnmatched: {len(unmatched)} questions (IDs: {unmatched[:20]}...)")
    else:
        print(f"\nUnmatched: {len(unmatched)} questions (IDs: {unmatched})")

    # 写入 questions.json
    with open(BASE / 'questions.json', 'w', encoding='utf-8') as f:
        json.dump(questions, f, ensure_ascii=False, indent=2)
    print(f"\n[DONE] Written questions.json ({len(questions)} questions)")

    # 写入 questions.js
    with open(BASE / 'questions.js', 'w', encoding='utf-8') as f:
        f.write('window.ALL_QUESTIONS = ' + json.dumps(questions, ensure_ascii=False, indent=2) + ';')
    print(f"[DONE] Written questions.js ({len(questions)} questions)")

    return questions, kp_counter


